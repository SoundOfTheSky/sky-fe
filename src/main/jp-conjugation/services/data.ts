export function toHiragana(kanji: string) {
  return kanji.replaceAll(/<ruby>.+?<rt>(.+?)<\/rt><\/ruby>/g, (_, h) => h as string);
}
export function removeFurigana(kanji: string) {
  return kanji
    .replaceAll(/<rt>.+?<\/rt>/g, '')
    .replaceAll('<ruby>', '')
    .replaceAll('</ruby>', '');
}
export function getVerbType(hiragana: string) {
  return [
    'える',
    'ける',
    'せる',
    'てる',
    'ねる',
    'へる',
    'める',
    'れる',
    'いる',
    'きる',
    'しる',
    'ちる',
    'にる',
    'ひる',
    'みる',
    'りる',
  ].some((x) => hiragana.endsWith(x)) && !['いる', 'はいる', 'かえる', 'はしる', 'へる', 'しゃべる'].includes(hiragana)
    ? 'v1'
    : 'v5';
}
export function getAdjectiveType(hiragana: string) {
  return hiragana.endsWith('い') && !['きれい', 'きらい', 'さいわい'].includes(hiragana) ? 'i' : 'na';
}
